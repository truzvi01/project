            <h3>What is a General Medical Practitioner (GP)?</h3>
            <p>A GP is your local personal NHS doctor.If you are ill or worried about your health or
                the health of anyone in your family, you should go and see your local GP.The place
                where you go to see your GP is called either a surgery or a health centre. GPs often
                work together in a small group called a practice.The benefit of GPs working in small
                groups is that if for any reason your own GP is not available, you may ask to see
                another GP in the same practice.</p>
            <p>Many practices have both male and female GPs. If your faith, religion or culture
                requires that you or a member of your family are treated by a GP of the same sex,
                please let the GP receptionist know this when you first go to register.</p>
            <p>Most surgeries and health centres are accessible by wheelchair, have doors that open
                electronically and have toilet facilities designed to meet the needs of disabled
                patients.</p>
            <h4>What services do GPs provide?</h4>
            <p>GPs are usually the first medical point of contact with the NHS. They are responsible
                for the comprehensive and continuing care of patients registered with them. GPs
                provide advice and treatment. If further treatment or investigation of a problem is
                required, the GP will coordinate this and ensure that it is provided. Further
                treatment might be provided by your GP, or by a member of their team such as a
                practice nurse, midwife or health visitor, or if required, by referral to a
                specialist doctor called a consultant or to other specialist services.</p>
            <p>GPs are also keen to promote good health amongst their patients. They and their staff
                give advice on diet, exercise, healthy living and disease prevention. </p>
            <p>Most patients are looked after by the same GP for many years. This builds up a bond
                of trust between the GP and patient and enables the GP to build up a good knowledge
                of you and your healthcare needs. </p>
